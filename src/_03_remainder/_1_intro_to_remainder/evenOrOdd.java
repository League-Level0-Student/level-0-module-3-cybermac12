package _03_remainder._1_intro_to_remainder;

public class evenOrOdd {
	public static void main(String[] args) {


		
			   int number = new Random().nextInt(100);
			    if (number % 2 == 0) {
			        System.out.println("number is even");
			    }
			    else {
			        System.out.println("number is odd");
			    }
			
		 

	}
}
